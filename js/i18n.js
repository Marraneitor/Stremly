/**
 * Get translated string by key
 */
function t(key, fallback) {
  const dict = translations[currentLang] || translations['es'];
  return dict[key] || (translations['es'][key]) || fallback || key;
}

/**
 * Apply translations to all data-i18n elements in the DOM
 */
function applyTranslations() {
  // Text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) {
      // Check if element has child elements we should preserve (like icons)
      const icon = el.querySelector('i.fa-solid, i.fa-brands, i.fa-regular');
      if (icon && el.children.length <= 2) {
        // Preserve the icon, update only the text
        const iconHtml = icon.outerHTML;
        el.innerHTML = iconHtml + ' ' + val;
      } else if (el.hasAttribute('data-i18n-html')) {
        el.innerHTML = val;
      } else {
        el.textContent = val;
      }
    }
  });

  // Placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key);
    if (val !== key) el.placeholder = val;
  });

  // Titles
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    const val = t(key);
    if (val !== key) el.title = val;
  });

  // Update HTML lang attribute
  document.documentElement.lang = currentLang === 'es' ? 'es' : currentLang === 'pt' ? 'pt' : currentLang === 'fr' ? 'fr' : 'en';

  // Update page title
  const titleMap = { es: 'Streamly â€” GestiÃ³n de Streaming', en: 'Streamly â€” Streaming Management', pt: 'Streamly â€” GestÃ£o de Streaming', fr: 'Streamly â€” Gestion de Streaming' };
  document.title = titleMap[currentLang] || titleMap['es'];

  // Update language selector button text
  const langBtn = document.getElementById('langCurrentLabel');
  if (langBtn) langBtn.textContent = currentLang.toUpperCase();
}

/**
 * Set language and apply
 */
function setLanguage(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('streamly_lang', lang);
  applyTranslations();
  // Update active state in dropdown
  document.querySelectorAll('.lang-option').forEach(el => {
    el.classList.toggle('active', el.dataset.lang === lang);
  });
  // Close dropdown
  const dd = document.getElementById('langDropdown');
  if (dd) dd.classList.remove('show');
}

/**
 * Toggle language dropdown
 */
function toggleLangDropdown() {
  const dd = document.getElementById('langDropdown');
  if (!dd) return;
  dd.classList.toggle('show');
}

// Close dropdown on outside click
document.addEventListener('click', (e) => {
  const dd = document.getElementById('langDropdown');
  const btn = document.getElementById('langToggleBtn');
  if (dd && btn && !btn.contains(e.target) && !dd.contains(e.target)) {
    dd.classList.remove('show');
  }
});

// Apply translations on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  applyTranslations();
});
